--DROP TABLE User IF EXISTS;

CREATE TABLE User (
  UserID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) PRIMARY KEY,
  LoginName VARCHAR(45),
  Password VARCHAR(20),
  FirstName VARCHAR(45),
  LastName VARCHAR(45),
  Gender VARCHAR(1),
  Email  VARCHAR(50),
  Phone VARCHAR(12),
  Address VARCHAR(255),
  City VARCHAR(20),
  Province VARCHAR(2),
  PostalCode VARCHAR(6),
  Role VARCHAR(45),
  Notify BOOLEAN
);